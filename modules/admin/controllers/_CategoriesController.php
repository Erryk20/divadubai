     /**
     * Delete multiple existing Categories model.
     * For ajax request will return json object
     * and for non-ajax request if deletion is successful, the browser will be redirected to the 'index' page.
     * @param integer $id
     * @return mixed
     */
    public function actionBulkDelete()
    {        
        $request = Yii::$app->request;
        $pks = explode(',', $request->post( 'pks' )); // Array or selected records primary keys
        
        $result = [];
        foreach ($pks as $value) {
            $item = explode('-', $value);
            
            $result[$item[0]][] = $item[1];
        }

        
        foreach ( $result as $id => $pk ) {
            
            foreach ($pk as $lan) {
                $model = CategoriesLan::findOne([
                    'category_id' => $id, 
                    'language'=>$lan
                ])->delete();
            }
            
            $model = CategoriesLan::findOne(['category_id' => $id]);

            if($model === null){
                Categories::findOne($id)->delete();
            }
        }

        if($request->isAjax){
            /*
            *   Process for ajax request
            */
            Yii::$app->response->format = Response::FORMAT_JSON;
            return ['forceClose'=>true,'forceReload'=>'#crud-datatable-pjax'];
        }else{
            /*
            *   Process for non-ajax request
            */
            return $this->redirect(['index']);
        }
       
    }
    
    public function saveModel($model){
        if($model->save(false)){
            $category_lan = CategoriesLan::findOne([
                'category_id' => $model->id, 
                'language' => $model->language
            ]);

            if(!$category_lan){
                $category_lan = new CategoriesLan();
                $category_lan->category_id = $model->id;
            }
            $category_lan->name = $model->name;
            $category_lan->language = $model->language;
            
            return $category_lan->save(false);
        }
    }
    

    /**
     * Finds the Categories model based on its primary key value.
     * If the model is not found, a 404 HTTP exception will be thrown.
     * @param integer $id
     * @return Categories the loaded model
     * @throws NotFoundHttpException if the model cannot be found
     */
    protected function findModel($id, $language)
    {
        $model = Categories::find()
                ->from('categories c')
                ->select(['c.id', 'c.url', 'cl.language', 'cl.name',  'c.created_at', 'c.updated_at', 'l.name AS language_name'])
                ->leftJoin('categories_lan cl', 'cl.category_id = c.id')
                ->leftJoin('language l', 'cl.language = l.short_name')
                ->where(['c.id' => $id, 'cl.language'=>$language])
                ->one();
        
        if (($model) !== null) {
            return $model;
        } else {
            throw new NotFoundHttpException('The requested page does not exist.');
        }
    }
